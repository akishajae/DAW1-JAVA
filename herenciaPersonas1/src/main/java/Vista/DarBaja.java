    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        btnBuscar.setSelected(true);

        try {
            try {
                personaSeleccionada = ControladorPersona.getPersonaByDNI((String) inputBuscar.getSelectedItem());
            } catch (DAO_Excep ex) {
                Logger.getLogger(DarBaja.class.getName()).log(Level.SEVERE, null, ex);
            }
            inputDNI.setText(personaSeleccionada.getDNI());
            inputNombre.setText(personaSeleccionada.getNombre());
            inputDireccion.setText(personaSeleccionada.getDireccion());
            inputEdad.setValue(personaSeleccionada.getEdad());

            switch (personaSeleccionada.getGenero()) {
                case 'f':
                    inputFem.setSelected(true);
                    break;
                case 'm':
                    inputMasc.setSelected(true);
                    break;
                case 'o':
                    inputOtro.setSelected(true);
                    break;
                default:
                    break;
            }

            //------------------------------------------------------------------
            if (!(personaSeleccionada.getVehiculo() == null)) {
                vehiculoLayout.show(panelVehiculo, "panelVehiculoSi");

                inputMatricula3.setText(personaSeleccionada.getVehiculo().getMATRICULA());
                inputAntiguedadSi.setValue(personaSeleccionada.getVehiculo().getAntiguedad());

            } else {
                vehiculoLayout.show(panelVehiculo, "panelVehiculoNo");
            }

            //------------------------------------------------------------------
            if (personaSeleccionada instanceof Empleado) {
                tipoLayout.show(panelEspecifico, "panelEmpleado");

                Empleado e = (Empleado) personaSeleccionada;
                inputIDempleado.setValue(e.getID_EMPLEADO());
                inputSueldo.setValue(e.getSueldo());

            } else if (personaSeleccionada instanceof Cliente) {
                tipoLayout.show(panelEspecifico, "panelCliente");

                Cliente c = (Cliente) personaSeleccionada;
                inputIDcliente.setValue(c.getID_CLIENTE());

                if (c.isVip()) {
                    siVIP.setSelected(true);
                } else {
                    noVIP.setSelected(true);
                }
            }

        } catch (excepcionPersona ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error cargando personas.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

