    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (btnBuscar.isSelected()) {
            try {
                int result = JOptionPane.showConfirmDialog(null, "¿Confirma que quiere dar de baja a esta persona?", "Confirmación", JOptionPane.YES_NO_OPTION);
                if (result == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(null, "La persona ha sido dada de baja.");
                    try {
                        ControladorPersona.darBaja(personaSeleccionada);
                    } catch (DAO_Excep ex) {
                        Logger.getLogger(DarBaja.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (result == JOptionPane.NO_OPTION) {
                    JOptionPane.showMessageDialog(null, "La persona no ha sido dada de baja.");
                }

            } catch (IOException ex) {
                Logger.getLogger(DarBaja.class.getName()).log(Level.SEVERE, null, ex);
            }

            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione a una persona.", "Buscar", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

